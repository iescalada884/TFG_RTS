#include <stdio.h>

char * gets(char *buf) {
  int i;
  int c;

  i = 0;
  while (1) {
    c = getchar();
    if (c == EOF)
      break;
    if (c == '\r' || c == '\n') {
      /* The FreeBSD manpage says:
       * The gets() function is equivalent to fgets() 
       * with an infinite size and a stream of stdin, 
       * except that the newline character (if any) 
       * is not stored in the string.
       */
      break;
    }
    if (c == '\b' || c == 0x7f) {
      if (i > 0) {
	i--;
      }
      continue;
    }
    buf[i++] = c;
  }
  buf[i] = '\0';
  return buf;
}

