#include <stdio.h>
#include <stdarg.h>
#include <stdlib.h>

/*
 * This function is called by the assert() macro defined in assert.h;
 * it's also a nice simple general-purpose panic function.
 */
void panic(const char *fmt, ...) 
{
  va_list vl;

  va_start(vl, fmt);
  vprinte(fmt, vl);
  va_end(vl);

  printe("\n");

  exit(1);
}

